def main():
    """Lee dos números y muestra suma, resta, multiplicación y división"""
    a = int(input())
    b = int(input())
    
    print(a + b)      # suma
    print(a - b)      # resta
    print(a * b)      # multiplicación
    print(a / b)      # división

if __name__ == "__main__":
    main()
